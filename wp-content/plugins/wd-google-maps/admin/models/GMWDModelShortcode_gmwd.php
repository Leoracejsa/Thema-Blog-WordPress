	public function get_maps(){
		global $wpdb;
		// get rows
		$query = "SELECT id, title  FROM " . $wpdb->prefix . "gmwd_maps WHERE published = '1' ORDER BY id";				
		$rows = $wpdb->get_results($query);
		$maps = array();
		foreach($rows as $row){
			$maps[$row->id] = $row->title;
		}
		return $maps;	
	}	

    public function get_shortcodes(){
        global $wpdb;
        // get rows
        $query = "SELECT *  FROM " . $wpdb->prefix . "gmwd_shortcodes ORDER BY id ASC";				
        $rows = $wpdb->get_results($query);
        return $rows;
    }	

    public function get_shortcode_max_id() {
        global $wpdb;
        $max_id = $wpdb->get_var("SELECT MAX(id) FROM " . $wpdb->prefix . "gmwd_shortcodes");
        return $max_id;
    }


